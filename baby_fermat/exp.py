from Crypto.Util.number import *
from gmpy2 import gmpy2

def fermat(n: int):
    y = 0
    while True:
        if gmpy2.iroot(4 * n + y * y, 2)[1]:
            break
        y += 1
    x = int(gmpy2.iroot(4 * n + y * y, 2)[0])
    return (x - y) // 2, (x + y) // 2

