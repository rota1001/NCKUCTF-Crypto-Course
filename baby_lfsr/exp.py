k = [2561736816123331519205225728225412625889126417863123128862759310696770577215157500948402752014111662885583666422884567177635253075802663898797129735728697, 13133048756018637146222933412405292753704464264612085963117040457970949630096254929176247838382217094698240710021279056520569555260520316433557966484001620]

k = (k[1] << 512) | k[0]
M = []
y = []

for i in range(512):
    state = (k >> i) & (2**512 - 1)
    target = (k >> (i + 512)) & 1
    y.append(target)
    M.append([(state >> j) & 1 for j in range(512)])

M = matrix(GF(2), M)
y = vector(GF(2), y)

sol = M.solve_right(y)
flag = sum(int(sol[i]) * 2**i for i in range(512))
print(long_to_bytes(int(flag)))
